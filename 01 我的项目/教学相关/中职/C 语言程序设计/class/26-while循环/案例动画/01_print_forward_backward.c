/*
 * 练习1：正向/反向输出数字
 * 要求1：打印1-5
 * 要求2：打印5-1
 *
 * 知识点：while循环的基本使用、循环条件设计
 */

#include <stdio.h>

int main() {
    printf("=== 正向/反向输出数字 ===\n\n");

    // 要求1：打印1-5（正向输出）
    printf("要求1：打印1-5\n");
    int i = 1;
    while (i <= 5) {
        printf("%d ", i);
        i++;
    }
    printf("\n\n");

    // 要求2：打印5-1（反向输出）
    printf("要求2：打印5-1\n");
    int j = 5;
    while (j >= 1) {
        printf("%d ", j);
        j--;
    }
    printf("\n");

    return 0;
}

/*
 * 运行结果：
 * === 正向/反向输出数字 ===
 *
 * 要求1：打印1-5
 * 1 2 3 4 5
 *
 * 要求2：打印5-1
 * 5 4 3 2 1
 *
 * 知识点总结：
 * 1. while循环的基本结构：初始化 → 判断条件 → 执行循环体 → 更新变量
 * 2. 正向输出使用 i++（递增）
 * 3. 反向输出使用 j--（递减）
 * 4. 循环条件要根据输出方向设计（<= 或 >=）
 */
