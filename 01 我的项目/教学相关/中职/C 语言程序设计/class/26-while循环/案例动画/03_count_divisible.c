/*
 * 练习3：统计满足条件的数字
 * 需求：键盘录入两个数字，表示一个范围。
 * 统计这个范围中，既能被6整除，又能被8整除的数字有多少个？
 *
 * 知识点：while循环+条件判断、取余运算符
 * 数学知识：既能被6整除又能被8整除 = 能被24整除（6和8的最小公倍数）
 */

#include <stdio.h>

int main() {
    printf("=== 统计满足条件的数字 ===\n\n");

    int start, end;
    printf("请输入两个数字，表示一个范围（例如：1 100）：\n");
    printf("起始数字：");
    scanf("%d", &start);
    printf("结束数字：");
    scanf("%d", &end);

    // 确保start <= end
    if (start > end) {
        int temp = start;
        start = end;
        end = temp;
    }

    int count = 0;  // 统计满足条件的数字个数
    int i = start;

    printf("\n满足条件的数字有：\n");

    while (i <= end) {
        // 既能被6整除，又能被8整除
        if (i % 6 == 0 && i % 8 == 0) {
            printf("%d ", i);
            count++;
        }
        i++;
    }

    printf("\n\n统计结果：在%d到%d之间，既能被6整除又能被8整除的数字有 %d 个\n",
           start, end, count);

    return 0;
}

