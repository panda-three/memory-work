/*
 * 练习4：水仙花数（100-999）
 * 打印所有的水仙花数
 *
 * 水仙花数定义：三位数，其各位数字的立方和正好等于该数本身。
 * 例如：153 = 1³ + 5³ + 3³ = 1 + 125 + 27 = 153
 *
 * 知识点：while循环、数字拆分（取余和除法）
 */

#include <stdio.h>

int main() {
    printf("=== 水仙花数（100-999） ===\n\n");
    printf("水仙花数是指三位数，其各位数字的立方和等于该数本身\n");
    printf("例如：153 = 1³ + 5³ + 3³ = 1 + 125 + 27\n\n");

    printf("所有的水仙花数有：\n");

    int num = 100;  // 从100开始
    int count = 0;  // 统计水仙花数的个数

    while (num <= 999) {
        // 拆分数字的各位
        int ge = num % 10;           // 个位：153 % 10 = 3
        int shi = num / 10 % 10;     // 十位：153 / 10 = 15, 15 % 10 = 5
        int bai = num / 100;         // 百位：153 / 100 = 1

        // 计算各位数字的立方和
        int sum = ge * ge * ge + shi * shi * shi + bai * bai * bai;

        // 判断是否是水仙花数
        if (sum == num) {
            printf("%d = %d³ + %d³ + %d³ = %d + %d + %d\n",
                   num, bai, shi, ge,
                   bai * bai * bai, shi * shi * shi, ge * ge * ge);
            count++;
        }

        num++;
    }

    printf("\n总共有 %d 个水仙花数\n", count);

    return 0;
}

