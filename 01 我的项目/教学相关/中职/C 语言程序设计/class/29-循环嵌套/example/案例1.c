#include <stdio.h>

/*
 * 最简单的嵌套循环示例
 * 目标:让学生看清楚"外循环一次,内循环全跑完"的规律
 */
int main() {
    printf("=== 外循环执行3次,内循环每次执行2次 ===\n\n");

    // 外循环:执行3次
     int i,j; 
    for ( i = 1; i <= 3; i++) {
        printf(">> 外循环:第%d次 开始\n", i);

        // 内循环:每次执行2次
        for ( j = 1; j <= 2; j++) {
            printf("   内循环:第%d次 (i=%d, j=%d)\n", j, i, j);
        }

        printf(">> 外循环:第%d次 结束\n\n", i);
    }

    printf("总结:外循环3次 × 内循环2次 = 总共执行了6次\n");

    return 0;
}

