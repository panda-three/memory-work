#include <stdio.h>

/*
 * 打印直角三角形(重点案例)
 * 难点:内循环的次数由外循环的变量i决定
 * 这是学生最容易卡住的地方!
 */
int main() {
    int height = 5;

    printf("=== 打印高度为%d的直角三角形 ===\n\n", height);
       int i,j; 
    // 外循环:控制行数(第1行到第5行)
    for ( i = 1; i <= height; i++) {
        printf("[第%d行]", i);

        // 【关键点】内循环的次数由i决定:
        // 第1行打印1个*, 第2行打印2个*, 第3行打印3个*...
        for ( j = 1; j <= i; j++) {
            printf(" *");
        }

        printf("\n");
    }

    printf("\n=== 执行过程详解 ===\n");
    printf("第1行: i=1, 内循环执行1次, 打印1个*\n");
    printf("第2行: i=2, 内循环执行2次, 打印2个*\n");
    printf("第3行: i=3, 内循环执行3次, 打印3个*\n");
    printf("第4行: i=4, 内循环执行4次, 打印4个*\n");
    printf("第5行: i=5, 内循环执行5次, 打印5个*\n");
    printf("\n关键点: 内循环的结束条件是 j <= i (不是固定值!)\n");

    return 0;
}

