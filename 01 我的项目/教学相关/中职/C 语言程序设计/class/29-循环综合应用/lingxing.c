#include <stdio.h>

/*
 * 练习3：菱形（挑战题）
 * 难度：????
 *
 * 要求：打印一个高度为9的菱形（上5行+下4行）
 * 预期输出：
 *     *           (第1行)
 *    ***          (第2行)
 *   *****         (第3行)
 *  *******        (第4行)
 * *********       (第5行) ← 最宽的一行
 *  *******        (第6行)
 *   *****         (第7行)
 *    ***          (第8行)
 *     *           (第9行)
 *
 * 思路：
 * 方法1：分成两部分
 *   - 上半部分：等腰三角形（5行）
 *   - 下半部分：倒等腰三角形（4行）
 *
 * 方法2：一个循环搞定（高级技巧）
 */

int main() {
    int i, j;
    int height = 5;  // 上半部分高度

    printf("=== 菱形图案 ===\n\n");

    // ===== 上半部分：等腰三角形（5行）=====
    printf("方法1：分两部分打印\n\n");

    // TODO: 补充上半部分代码（参考等腰三角形）
    for (i = 1; i <= height; i++) {
        // 打印空格
        for (j = 1; j <= height - i; j++) {
            printf(" ");
        }

        // 打印星号
        for (j = 1; j <= 2 * i - 1; j++) {
            printf("*");
        }

        printf("\n");
    }

    // ===== 下半部分：倒等腰三角形（4行）=====
    // TODO: 补充下半部分代码
    // 提示：
    // - 行数是4行（height - 1）
    // - 空格从少到多
    // - 星号从多到少
    //
     for (i = 1; i <= height - 1; i++) {
//         打印空格：i个空格
         for (j = 1; j <= i; j++) {
             printf(" ");
         }
    
//         打印星号：递减的奇数
         for (j = 1; j <= 2 * (height - i) - 1; j++) {
             printf("*");
         }
    
         printf("\n");
     }

    return 0;
}

